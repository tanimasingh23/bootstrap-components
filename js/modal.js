$( document ).ready(function() {

   $(".btn").click(() => {
    $(".modal").css("display","block")

});


$(".close").click(() => {
    $(".modal").css("display","none")

});

$(".ok").click(() => {
    $(".modal").css("display","none")

});
$(".cancel").click(() => {
    $(".modal").css("display","none")

});
 });
